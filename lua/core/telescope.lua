-- This file provides reference for telescope keymaps and pickers

-- Telescope setup and keymaps are in plugins.lua for lazy-loading
-- Main keymaps:
-- <leader>f - Find files
-- <leader>g - Live grep (search in files)

-- Built-in keymaps when telescope is open:
-- <C-n>/<Down> - Next item
-- <C-p>/<Up>   - Previous item
-- <CR>         - Confirm selection
-- <C-x>        - Open in horizontal split
-- <C-v>        - Open in vertical split
-- <C-t>        - Open in new tab
-- <C-u>        - Scroll preview up
-- <C-d>        - Scroll preview down
-- <C-c>        - Close telescope
-- <Esc>        - Close telescope (normal mode)
-- <Tab>        - Toggle selection
-- <C-q>        - Send selected to quickfix list

-- Available pickers (can be accessed via :Telescope <picker>):
-- find_files, live_grep, grep_string, buffers, help_tags
-- oldfiles, commands, command_history, search_history
-- git_files, git_commits, git_bcommits, git_branches, git_status
-- lsp_references, lsp_definitions, lsp_document_symbols, lsp_workspace_symbols
